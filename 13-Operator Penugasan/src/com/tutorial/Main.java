public class Main {
    public static void main( String[] args){
        
        //Penugasan Operator
        
        //Jumlah operator penugasan
        int a = 1;
        a += 10; // a = a + 10;
        System.out.println("nilai a = " + a);
        
        //Operator penugasan Pengaturan
        int b = 100;
        b -= 25;
        System.out.println("nilai b = " + b);
        
        //Operator tugas penilaian
        int c = 100;
        c *= 20;
        System.out.println("nilai c = " + c);
        
        //Operator pembagian tugas
        int d = 100;
        d /= 20;
        System.out.println("nilai d = " + d);
        
        //Modulus operator penugasan
        int e = 10;
        e %= 7;
        System.out.println("nilai e = " + e);     
    }
}
